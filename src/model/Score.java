package model;

public class Score {

	private String name;
	private double score;

	/**
	 * 
	 * @param name
	 * @param score
	 */
	public Score(String name, double score) {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getScore() {
		return score;
	}

	public void setScore(double score) {
		this.score = score;
	}

}